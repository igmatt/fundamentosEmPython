#Operadores Aritiméticos
valor1 = 10
valor2 = 5
soma = valor1 + valor2
subtracao = valor1 - valor2
multiplicacao = valor1 * valor2
divisao = valor1 / valor2
restoDivisao = valor1&valor2

print('Soma: ',soma, '\nSubtracao:', subtracao, '\nMultiplicação: ', multiplicacao, '\nDivisao: ', divisao, '\nResto da Divisão: ', restoDivisao)

